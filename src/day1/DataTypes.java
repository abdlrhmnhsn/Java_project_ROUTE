package day1;

public class DataTypes {
    public static void main(String[] args) {
        byte by =127;
        System.out.println("The value of byte variable is "+by);
        short sh= 32767;
        System.out.println("The value of short variable is "+sh);
        int i= 52767;
        System.out.println("The value of int variable is "+i);
        long l= 5274447L;
        System.out.println("The value of long variable is "+l);
        float fl= 527.587F;
        System.out.println("The value of float variable is "+fl);
        double dbl= 527.5888887;
        System.out.println("The value of double variable is "+dbl);
        boolean bl = true;
        System.out.println("The value of boolean variable is "+bl);
        char ch = 'A';
        System.out.println("The value of character variable is "+ch);
        String st = "Abdelrahman";
        System.out.println("The value of string object is "+st);


    }
    }
